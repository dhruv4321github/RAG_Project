@router.get("/audit-log", response_model=list[AuditLogResponse])
def view_audit_log(
    limit: int = 50,
    offset: int = 0,
    action_type: Optional[str] = None,
    db: Session = Depends(get_db),
):
    """
    View the audit trail of all LLM interactions.

    This endpoint is essential for compliance — it allows compliance officers
    to review every question asked, every document chunk retrieved, and
    every response generated by the AI.

    Filter by action_type: "chat_query", "report_summary", "report_risk_note", "report_client_email"
    """
    logs = get_audit_logs(db, limit=limit, offset=offset, action_type=action_type)
    return logs
