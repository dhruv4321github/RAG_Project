"""
reports.py — Report Generation & Audit Log API Routes

Endpoints:
  POST /api/reports/summary      — Generate a portfolio summary
  POST /api/reports/risk-note    — Generate a risk assessment note
  POST /api/reports/client-email — Draft a client email
  GET  /api/reports/audit-log    — View the full audit trail
"""

from typing import Optional

from fastapi import APIRouter, Depends, HTTPException
from sqlalchemy.orm import Session

from app.models.database import get_db
from app.models.schemas import ReportRequest, ReportResponse, AuditLogResponse
from app.services.report_generator import generate_report
from app.services.audit_logger import get_audit_logs

router = APIRouter()


@router.post("/summary", response_model=ReportResponse)
def create_summary(request: ReportRequest, db: Session = Depends(get_db)):
    """
    Generate a portfolio summary from uploaded documents.
    Includes executive overview, asset allocation, key holdings, and observations.
    """
    try:
        result = generate_report(
            db=db,
            report_type="summary",
            document_ids=request.document_ids,
            additional_instructions=request.additional_instructions,
        )
        return ReportResponse(**result)
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


